# Integer variables
num1 = 30
num2 = 55

# Division 
result = num1 / num2   #value=2.0   
print("Integer Division:", result)

# Division 
result = num1 // num2   #value=2  
print("Integer Division:", result)

# Modulus (Remainder)
result = num1 % num2
print("Modulus (Remainder):", result)

# Absolute Value
result = abs(-7)
print("Absolute Value:", result)

# Float variables
num1 = 7.0
num2 = 6.5


result = num1 + num2
print("Addition:", result)

result = num1 - num2
print("Subtraction:", result)

result = num1 * num2
print("Multiplication:", result)

result = num1 / num2
print("Division:", result)


result = round(3.14159265359, 2)  # Rounds to 2 decimal places
print("Rounded:", result)